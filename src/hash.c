#include <libftprintf.h>

void	hash(t_spf *esp, char c, char *nn)
{
	if (!(esp->len == 1 && *nn == '0') && esp->hash && (c == 'x' || c == 'X'))
	{
		if (c == 'x')
			esp->count += write(1, "0x", 2);
		else if (c == 'X')
			esp->count += write(1, "0X", 2);
	}
	return ;
}
	
